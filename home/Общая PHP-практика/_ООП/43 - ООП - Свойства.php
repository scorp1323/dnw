Мы распечатали 2 обьекта - машина и машина.
	они обладают одинаковымы свойствами (цвет, скорость, расход топлива, бренд) но разными значениями этих свойств (белая/черная, 110/130, 12/14, Audi/Mersedes)
Для считывания значения свойств используют конструкцию
	 echo car1 -> color;  // выведет black

ТИпами данных у свойств (как у переменных) могут быть:
	- int
	- float
	- array
	- object

